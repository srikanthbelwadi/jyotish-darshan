for lang_code, text_dict in UI_TEXT.items():
    lang_block_pattern = r"(export const DYNAMIC_STRINGS\s*=\s*\{.*?['\"]" + lang_code + r"['\"]\s*:\s*\{)(.*?)(\},?\s*(['\"][a-z]{2,3}['\"]\s*:|\}\s*;))"
    
    def replacer(match):
        header = match.group(1)
        body = match.group(2)
        footer = match.group(3)
        
        inserts = []
        for key, val in text_dict.items():
            if f'"{key}"' not in body and f"'{key}'" not in body:
                safe_val = val.replace('"', '\\"')
                inserts.append(f'"{key}": "{safe_val}",')
        
        if lang_code in L_ABBR:
            for pl_key, abbr_val in L_ABBR[lang_code].items():
                k = f"pl.abbr.{pl_key}"
                if f'"{k}"' not in body and f"'{k}'" not in body:
                    safe_val = abbr_val.replace('"', '\\"')
                    inserts.append(f'"{k}": "{safe_val}",')

        if not inserts:
            return match.group(0)

        inserts_str = "\n    " + "\n    ".join(inserts)
        
        if body.strip().endswith(","):
            return header + body + inserts_str + footer
        else:
            return header + body + ",\n" + inserts_str + footer

    content = re.sub(lang_block_pattern, replacer, content, flags=re.DOTALL)

with open(file_path, "w", encoding="utf-8") as f:
    f.write(content)
