for lang_code, nak_list in translations.items():
    lang_block_pattern = r"(export const DYNAMIC_STRINGS\s*=\s*\{.*?['\"]" + lang_code + r"['\"]\s*:\s*\{)(.*?)(\},?\s*(['\"][a-z]{2,3}['\"]\s*:|\}\s*;))"
    
    def replacer(match):
        header = match.group(1)
        body = match.group(2)
        footer = match.group(3)
        
        # Add English keys as default if missing from other array
        inserts = []
        for i, nak in enumerate(nak_list):
            if f'"astro.nakshatras.{i}"' not in body and f"'astro.nakshatras.{i}'" not in body:
                inserts.append(f'"{str(i)}": "{nak}",')
                inserts.append(f'"astro.nakshatras.{str(i)}": "{nak}",')

        # Add generic keys just in case
        inserts_str = "\n    " + "\n    ".join(inserts)
        
        if body.strip().endswith(","):
            return header + body + inserts_str + footer
        else:
            return header + body + ",\n" + inserts_str + footer

    content = re.sub(lang_block_pattern, replacer, content, flags=re.DOTALL)

with open(file_path, "w", encoding="utf-8") as f:
    f.write(content)
